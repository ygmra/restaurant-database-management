    public ArrayList<relationshift> relationshift(){
    
    ArrayList <relationshift> relationshift= new ArrayList <>();
        
   
    try{
        
        Class.forName("com.mysql.jdbc.Driver");
        
        Connection conn =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurantdbase?zeroDateTimeBehavior=convertToNull","root","1234567");
        String query="select * from relationshift";
        
        
        Statement st= (Statement) conn.createStatement();
        ResultSet rs= st.executeQuery(query);
        relationshift Relationshift;
        while(rs.next()){
            Relationshift = new relationshift (rs.getInt("RelationSID"),rs.getInt("WaiterID"),rs.getInt("ShiftID"));
            relationshift.add(Relationshift);
            
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
    }
    return relationshift;
}
