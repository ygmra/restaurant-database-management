    public ArrayList<R_Waiter_Lang> R_Waiter_LangList(){
    
    ArrayList <R_Waiter_Lang> R_Waiter_Lang= new ArrayList <>();
        
   
    try{
        
        Class.forName("com.mysql.jdbc.Driver");
        String url= "jdbc:sqlserver://localhost:3306/mysql?server=UTC";
        Connection conn =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurantdbase?zeroDateTimeBehavior=convertToNull","root","1234567");
        String query="select * from relationlangwaiter";
        
        
        Statement st= (Statement) conn.createStatement();
        ResultSet rs= st.executeQuery(query);
        R_Waiter_Lang r_Waiter_Lang;
        while(rs.next()){
            r_Waiter_Lang = new R_Waiter_Lang (rs.getInt("RelationLangWaiterID"),rs.getInt("WaiterID"),rs.getInt("LanguageID"));
            R_Waiter_Lang.add(r_Waiter_Lang);
            
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
    }
    return R_Waiter_Lang;
}
