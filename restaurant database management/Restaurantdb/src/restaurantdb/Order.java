    public ArrayList<Menu> MenuList(){
    
    ArrayList <Menu> Menu= new ArrayList <>();
        
   
    try{
        
        Class.forName("com.mysql.jdbc.Driver");
        
        Connection conn =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/restaurantdbase?zeroDateTimeBehavior=convertToNull","root","1234567");
        String query="select FoodNamesID,Name,Price from foodnames";
        
        
        Statement st= (Statement) conn.createStatement();
        ResultSet rs= st.executeQuery(query);
        Menu menu;
        while(rs.next()){
            menu = new Menu (rs.getInt("FoodNamesID"),rs.getString("Name"),rs.getInt("Price"));
            Menu.add(menu);
            
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null,e);
    }
    return Menu;
}
